function getTotal(opponentChoices, myChoices) {
	let pointByPlay = getPointCountByPlay(myChoices);
	let pointByWin = getPointCountByWin(opponentChoices, myChoices);
	return pointByPlay + pointByWin;
}

// 計算透過出拳得分的總和
function getPointCountByPlay(arr) {
	let sum = 0;
	for (let i = 0; i < arr.length; i++) {
		let current = arr[i];
		if (current == "rock") {
			sum += 1;
		} else if (current == "paper") {
			sum += 2;
		} else if (current == "scissor") {
			sum += 3;
		}
	}
	return sum;
}

// 計算透過比賽結果的得分總合
function getPointCountByWin(arr1, arr2) {
	let sum = 0;
	for (let i = 0; i < arr1.length; i++) {
		let point = winOrLose(arr1[i], arr2[i]);
		sum += point;
	}
	return sum;
}

// 判斷輸贏，並回傳分數
function winOrLose(opponentChoice, myChoice) {
	let WinPoint = 6;
	let losePoint = 0;
	let drawPoint = 3;
	let point;
	if (opponentChoice === myChoice) {
		return (point = drawPoint);
	} else if (opponentChoice === "rock") {
		point = myChoice === "paper" ? WinPoint : losePoint;
	} else if (opponentChoice === "paper") {
		point = myChoice === "scissor" ? WinPoint : losePoint;
	} else if (opponentChoice === "scissor") {
		point = myChoice === "rock" ? WinPoint : losePoint;
	}
	return point;
}
